.global shift_cmd_test

shift_cmd_test:
    //w0= 0B 1000 0000 0000 ... 0011
    ldr w0, =0x80000003
    //逻辑x移,会丢弃
    lsl w1, w0, 1 // 0B 0000 0000 ... 0110
    lsr w2, w0, 1 // 0B 0100 0000 ... 0001
    // 算术右移, 最高位补符号位
    asr w3, w0, 1 // 0B 1100 0000 ... 0001
    // 循环右移
    ror w4, w0, 1 // 0B 1100 0000 ... 0001

    ret
